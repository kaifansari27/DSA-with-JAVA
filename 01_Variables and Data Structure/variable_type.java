public class variable_type {
    public static void main(String[] args) {
        byte b = 9;
        System.out.println(b);
        char ch = 'K';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        int number =  27 ;
        System.out.println(number);
    }
    
}
